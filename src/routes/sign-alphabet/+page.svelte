<h1>Česká jednoruční protová abeceda</h1>

<p>
    Prstová abeceda je komunikační systém, vycházející z mluveného jazyka (v České republice primárně z češtiny). Tento systém je užíván v komunikaci neslyšících osob (mezi sebou nebo se slyšícími osobami), ale téměř nikdy není používán samostatně. Může fungovat např. jako podpora a doplněk k odezírání, nebo figurovat v komunikaci, v níž se používá znakový jazyk[1] – je totiž také součástí mnoha přirozených znakových jazyků jako důsledek kontaktu mezi znakovými a mluvenými jazyky. [2][3] Může být používána i ke komunikaci slyšících osob v hlučných provozech a prostředích, při bojových hrách nebo jako způsob napovídání ve škole.
</p>

<p>
    Prstová abeceda využívá formalizovaných a ustálených postavení prstů a dlaně jedné ruky nebo prstů a dlaní obou rukou k zobrazování jednotlivých písmen abecedy. Je využívána zejména k odhláskování cizích slov, odborných termínů, případně dalších pojmů. Prstová abeceda v taktilní (hmatové) formě může být využívána jako komunikační systém hluchoslepých osob.[5]
</p>

